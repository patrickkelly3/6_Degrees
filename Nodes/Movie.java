package Nodes;

public class Movie {
    private String id;
    private String name;

    public String getID() {
        return this.id;
    }

    public String getName() {
        return this.name;
    }

    public void setID(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
